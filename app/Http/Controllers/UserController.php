     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
         $validator = $request->validate([
            'email' => 'required',
            'name' => 'required',
      ]);

      $requestParams = $request->all();


      $saveArray = [];


      $saveArray['email'] = $requestParams['email'];
      $saveArray['name'] = $requestParams['name'];

      $userId = \Auth::user()->id;
      if(isset($requestParams['u'])) {
        $userId = $requestParams['u'];
      }


      User::updateUserById($userId, $saveArray);

      return redirect()->back()->with('success', "User details updated");
    }

